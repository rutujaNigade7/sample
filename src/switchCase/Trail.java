package org.switchCase;

public class Trail {

    public static void main(String args[]){

        int k =65;
        switch(k){
            default :
                System.out.println("website");
            case 65 :
                System.out.println("Merit");
            case 'k' :
                System.out.println("campus");
            case 'j' :
                System.out.println("java");
                break;
        }
    }
}
